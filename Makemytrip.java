package com.xworkz.constructor.things;

public class Makemytrip {
	private String hotels;
	private String flights;
	private String trains;
	private String holidayPackages;
	private String homeStays;
	private String travelInsurance;
	private String giftCards;
	private String airportCabs;
	private String tripIdeas;
	private String activities;
	
	
	public Makemytrip() {
		System.out.println("No-argument constructor");
	}
	
	public void setHotels(String hotels) {          
		this.hotels=hotels;
	}
	public String getHotels() {
		return hotels;
	}
	public void setFlights(String flights) {          
		this.flights=flights;
	}
	public String getFlights() {
		return flights;
	}
	public void setTrains(String trains) {          
		this.trains=trains;
	}
	public String getTrains() {
		return trains;
	}
	public void setHolidayPackages(String holidayPackages) {          
		this.holidayPackages=holidayPackages;
	}
	public String getHolidayPackages() {
		return holidayPackages;
	}
	public void setHomeStays(String homeStays) {          
		this.homeStays=homeStays;
	}
	public String getHomeStays() {
		return homeStays;
	}
	public void setTravelInsurance(String travelInsurance) {          
		this.travelInsurance=travelInsurance;
	}
	public String getTravelInsurance() {
		return travelInsurance;
	}
	public void setGiftCards(String giftCards) {          
		this.giftCards=giftCards;
	}
	public String getGiftCards() {
		return giftCards;
	}
	public void setAirportCabs(String airportCabs) {          
		this.airportCabs=airportCabs;
	}
	public String getAirportCabs() {
		return giftCards;
	}
	public void setTripIdeas(String tripIdeas) {          
		this.tripIdeas=tripIdeas;
	}
	public String getTripIdeas() {
		return tripIdeas;
	}
	public void setActivities(String activities) {          
		this.activities=activities;
	}
	public String getActivities() {
		return activities;
	}

}
